<script>
  const $demos = document.querySelector('#docs-demos');
  const $demo = $demos.querySelector('.docs-demo.is-active');
  const $square = $demo.querySelector('.square');

  const animatableSquare = {
    move(x, y) {
      anime({
        targets: $square,
        translateX: x,
        translateY: y,
        duration: 300,
        easing: 'easeOutCubic'
      });
    }
  };

  const onMouseMove = (e) => {
    const bounds = $demo.getBoundingClientRect();
    const x = e.clientX - bounds.left - 40;
    const y = e.clientY - bounds.top - 40;
    animatableSquare.move(x, y);
  };

  window.addEventListener('mousemove', onMouseMove);
</script>

